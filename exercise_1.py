def convert_to_int(value):
    ''' 1. При передаче строки `"123"` функция должна успешно преобразовать её в целое число и вывести результат.
    2. При передаче строки `"abc"` функция должна вывести сообщение о том, что преобразование невозможно.
    3. При передаче списка `[1, 2, 3]` функция должна вывести сообщение о неожиданной ошибке, так как список не может быть преобразован в целое число.
    '''

    try:
        # Попробуем преобразовать значение в целое число
        result = int(value)
        print(f"Преобразование успешно: {result}")
        return result
    except ValueError:
        # Обработка случая, когда преобразование невозможно
        print("Ошибка: невозможно преобразовать строку в целое число.")
    except Exception as e:
        # Обработка любых других исключений
        print(f"Неожиданная ошибка: {e}")
    finally:
        # Блок finally выполняется в любом случае
        print("Завершение попытки преобразования.")

# Проверка функции с разными входными значениями
print("Тест с корректной строкой '123':")
convert_to_int("123")

print("\nТест с некорректной строкой 'abc':")
convert_to_int("abc")

print("\nТест с другим типом данных [1, 2, 3]:")
convert_to_int([1, 2, 3])